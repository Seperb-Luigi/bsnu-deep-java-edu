package Laba_02;

public class Laba_02 {
    public static void main(String[] args) {
        char sunSymbol = '\u2609';
        char chessKing = '\u2654';
        char musicalNote = '\u266B';
        char smilingFace = '\u263A';
        char yinYang = '\u262F';



        System.out.println("Stored Unicode Characters using Escape Sequences:");
        System.out.println("Sun Symbol: " + sunSymbol);
        System.out.println("Chess King: " + chessKing);
        System.out.println("Musical Note: " + musicalNote);
        System.out.println("Smiling Face: " + smilingFace);
        System.out.println("Yin Yang: " + yinYang);
    }
}